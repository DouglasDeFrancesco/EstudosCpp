// Tipos.cpp
// Ilustra os tipos
// básicos de C++.
#include <iostream>
using namespace std;
int main()
{
// Declara e
// inicializa uma
// variável char.
char cVar = 't';
// Declara e
// inicializa uma
// variável int.
int iVar = 298;
// Declara e
// inicializa uma
// variável float.
float fVar = 49.95;
// Declara e
// inicializa uma
// variável double.
double dVar = 99.9999;
// Declara e
// inicializa uma
// variável bool.
bool bVar = (2 > 3); // False.
// O mesmo que:
// bool bVar = false;
// Exibe valores.
cout << "cVar = "
<< cVar << "\n";
cout << "iVar = "
<< iVar << "\n";
cout << "fVar = "
<< fVar << "\n";
cout << "dVar = "
<< dVar << "\n";
cout << "bVar = "
<< bVar << "\n";
return 0;
} // Fim de main()
